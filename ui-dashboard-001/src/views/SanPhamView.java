    Sanpham readform() {
        String masp, tensp, mota;
        int idlsp;
        masp = txtmasp.getText();
        tensp = txttensp.getText();
        mota = txtmota.getText();
        idlsp = Integer.parseInt(cbbLoaisp.getSelectedItem().toString()); // Chuyển String → int

        return new Sanpham(i, masp, tensp, mota, idlsp, Boolean.TRUE);
    }
