    private void filterTable(String vaiTro, String trangThai) {
        List<NhanVien> filteredList = new ArrayList<>();
        for (NhanVien nv : rp.getAll()) {
            boolean matchesVaiTro = vaiTro.equals("Tất cả") || (nv.isVaiTro() && vaiTro.equals("Quản lý")) || (!nv.isVaiTro() && vaiTro.equals("Nhân viên"));
            boolean matchesTrangThai = trangThai.equals("Tất cả") || (nv.isTrangThai() && trangThai.equals("Đang làm")) || (!nv.isTrangThai() && trangThai.equals("Đã nghỉ"));

            if (matchesVaiTro && matchesTrangThai) {
                filteredList.add(nv);
            }
        }
        fillTable((ArrayList<NhanVien>) filteredList);
    }

    public void showData() {
        int row = tblQLNV.getSelectedRow();
        txtMaNV.setText(tblQLNV.getValueAt(row, 1).toString());
        txtHoTen.setText(tblQLNV.getValueAt(row, 2).toString());
        if (tblQLNV.getValueAt(row, 3).toString().equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtSDT.setText(tblQLNV.getValueAt(row, 4).toString());
        txtEmail.setText(tblQLNV.getValueAt(row, 5).toString());
        cboVaiTro.setSelectedItem(tblQLNV.getValueAt(row, 6).toString());
        cboTrangThai.setSelectedItem(tblQLNV.getValueAt(row, 7).toString());
    }

