    public void fillTable(ArrayList<NhanVien> list) {
        mol = (DefaultTableModel) tblQLNV.getModel();
        mol.setRowCount(0);
        for (NhanVien nv : list) {
            mol.addRow(new Object[]{
                nv.getId(),
                nv.getMaNhanVien(),
                nv.getTen(),
                nv.isGioiTinh() ? "Nam" : "Nữ",
                nv.getSoDienThoai(),
                nv.getEmail(),
                nv.isVaiTro() ? "Quản lý" : "Nhân viên",
                nv.isTrangThai() ? "Đang làm" : "Đã nghỉ"
            });
        }
    }

